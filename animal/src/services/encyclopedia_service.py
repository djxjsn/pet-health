class EncyclopediaService:
    """知识科普服务 - 提供品种和健康知识的检索"""

    @staticmethod
    def get_breeds(species: str) -> List[dict]:
        if species == "cat":
            data = CAT_BREEDS
        elif species == "dog":
            data = DOG_BREEDS
        else:
            return []
        result = [BreedSummary(**{
            "id": b["id"], "name": b["name"],
            "english_name": b.get("english_name", ""),
            "species": b["species"],
            "summary": b["summary"],
            "image_emoji": b.get("image_emoji", "🐾"),
            "popularity": b.get("popularity", 5)
        }).model_dump() for b in data]
        result.sort(key=lambda x: x["popularity"], reverse=True)
        return result

    @staticmethod
    def get_breed_detail(breed_id: str) -> Optional[dict]:
        all_breeds = CAT_BREEDS + DOG_BREEDS
        for b in all_breeds:
            if b["id"] == breed_id:
                return BreedDetail(**b).model_dump()
        return None

    @staticmethod
    def get_health_conditions(species: str) -> List[dict]:
        if species == "cat":
            conditions = CAT_HEALTH_CONDITIONS
        elif species == "dog":
            conditions = DOG_HEALTH_CONDITIONS
        elif species == "both":
            conditions = CAT_HEALTH_CONDITIONS + DOG_HEALTH_CONDITIONS
        else:
            return []

        grouped = {}
        for c in conditions:
            cat = c["category"]
            if cat not in grouped:
                label_map = {
                    "digestive": "消化系统", "respiratory": "呼吸系统",
                    "skin": "皮肤系统", "urinary": "泌尿系统",
                    "eye": "眼部疾病", "dental": "口腔疾病",
                    "parasite": "寄生虫", "infectious": "传染病",
                    "skeletal": "骨骼关节",
                }
                grouped[cat] = {
                    "category": cat,
                    "category_label": label_map.get(cat, cat),
                    "conditions": []
                }
            grouped[cat]["conditions"].append(HealthConditionSummary(**{
                "id": c["id"], "name": c["name"],
                "species": c["species"],
                "category": c["category"],
                "severity": c["severity"],
                "image_emoji": c.get("image_emoji", "🏥"),
            }).model_dump())
        return [HealthCategoryGroup(**g).model_dump() for g in grouped.values()]

    @staticmethod
    def get_health_detail(condition_id: str) -> Optional[dict]:
        all_conditions = CAT_HEALTH_CONDITIONS + DOG_HEALTH_CONDITIONS
        for c in all_conditions:
            if c["id"] == condition_id:
                return HealthCondition(**c).model_dump()
        return None

    @staticmethod
    def search_knowledge(query: str) -> dict:
        q = query.lower()

        matched_breeds = []
        all_breeds = CAT_BREEDS + DOG_BREEDS
        for b in all_breeds:
            if (q in b["name"].lower() or q in b.get("english_name", "").lower()
                    or any(q in p.lower() for p in b.get("personality", []))):
                matched_breeds.append(BreedSummary(**{
                    "id": b["id"], "name": b["name"],
                    "english_name": b.get("english_name", ""),
                    "species": b["species"],
                    "summary": b["summary"],
                    "image_emoji": b.get("image_emoji", "🐾"),
                    "popularity": b.get("popularity", 5),
                }).model_dump())

        matched_conditions = []
        all_conditions = CAT_HEALTH_CONDITIONS + DOG_HEALTH_CONDITIONS
        for c in all_conditions:
            if (q in c["name"].lower() or any(q in s.lower() for s in c.get("symptoms", []))
                    or any(q in s.lower() for s in c.get("possible_causes", []))):
                matched_conditions.append(HealthConditionSummary(**{
                    "id": c["id"], "name": c["name"],
                    "species": c["species"],
                    "category": c["category"],
                    "severity": c["severity"],
                    "image_emoji": c.get("image_emoji", "🏥"),
                }).model_dump())

        return {
            "breeds": matched_breeds[:5],
            "conditions": matched_conditions[:5],
        }
