# 这里用颜色来类比说明：相似的颜色 → 相似的向量 → 相近的距离
# 我们用 (r, g, b) 三个数字来表示一个颜色

# 定义一个函数：计算两个颜色向量之间的"欧几里得距离"
# 
# 欧几里得距离 (euclidean distance)：
#   就是平面上两点之间的直线距离的推广。
#   二维：sqrt((x1-x2)² + (y1-y2)²)
#   三维：sqrt((r1-r2)² + (g1-g2)² + (b1-b2)²)
#   距离越小 → 两个东西越相似
def cal_color_distance(color1, color2):
    """
    计算两个颜色的相似程度。
    
    参数:
        color1: 一个包含3个数字的列表，代表 (红色值, 绿色值, 蓝色值)
                每个值的范围是 0 到 255，比如 [255, 0, 0] 是纯红色
        color2: 同上，另一个颜色的 rgb 值
    
    返回:
        一个数字（float类型），表示两个颜色之间的距离。
        数字越小 = 两个颜色越像
        数字越大 = 两个颜色越不像
    
    举例:
        cal_color_distance([255,0,0], [250,5,5]) → 6.48  （两个红色，很近）
        cal_color_distance([255,0,0], [0,255,0]) → 360.6  （红 vs 绿，很远）
    """
    
    # zip() 是 python 内置函数，作用是把两个列表"拉链"在一起
    # 比如 zip([1,2,3], [4,5,6]) 会生成 (1,4), (2,5), (3,6) 这样的对
    # 这里用它来同时取出两个颜色在同一位置的值
    # 
    # sum() 是 python 内置函数，作用是把列表里所有数字加起来
    # sum([1, 2, 3]) → 6
    # 
    # 这里 sum(...) 计算的是 "每个维度上差值的平方和"
    # 比如两个颜色分别是 (r1,g1,b1) 和 (r2,g2,b2)
    # 结果 = (r1-r2)² + (g1-g2)² + (b1-b2)²
    sum_of_squares = sum(
        # a 是 color1 中某个位置的值（比如红色值）
        # b 是 color2 中同一位置的值（比如也是红色值）
        (a - b) ** 2
        for a, b in zip(color1, color2)
    )
    
    # math.sqrt() 是 python 内置函数，作用是求平方根
    # math.sqrt(9) → 3.0
    # math.sqrt(2) → 1.414...
    # 这里对差值的平方和开根号，得到最终的欧几里得距离
    return math.sqrt(sum_of_squares)


