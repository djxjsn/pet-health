# ================================================================================
#  第二部分：文字怎么变成向量？—— 词袋模型（最朴素的方法）
# ================================================================================
#  现在把"颜色"替换成"文字"。
#  
#  最简单的文字→向量方法叫"词袋模型" (bag of words)。
#  
#  原理：
#    1. 收集你要处理的全部句子
#    2. 把所有出现过的词列成一个"词汇表"
#    3. 每个句子用一个向量表示：向量的每一位 = 词汇表中对应词出现的次数
#  
#  举例：
#    词汇表 = ["猫", "狗", "吃", "鱼", "睡觉"]
#    "猫吃鱼"     →  [1, 0, 1, 1, 0]   （猫出现1次, 吃出现1次, 鱼出现1次）
#    "狗睡觉"     →  [0, 1, 0, 0, 1]   （狗出现1次, 睡觉出现1次）
#    "猫吃猫粮"   →  [1, 0, 1, 0, 0]   （猫出现1次, 吃出现1次, 猫粮不在词汇表）
#  
#  这个方法的缺点：
#    - 向量的大小 = 词汇表的大小，词汇表越大向量越长
#    - 无法理解"猫"和"猫咪"是同义词（只统计词频，不懂含义）
#    - 但是！它够简单，适合入门理解
# ================================================================================

