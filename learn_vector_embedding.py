def build_vocabulary(all_sentences):
    """
    从所有句子中提取词汇表。
    
    词汇表 (vocabulary) 是什么？
      - 一个列表，包含了所有句子中出现过的、不重复的词语
      - 每个词在列表中有一个固定的位置（索引）
      - 后面每个句子都会根据这个词汇表来生成向量
    
    参数:
        all_sentences: 一个列表，每个元素是一个字符串（一句话）
           比如 ["猫吃鱼", "狗吃骨头"]
    
    返回:
        一个列表，包含所有不重复的、排序后的词汇
           比如 ["吃", "猫", "鱼", "狗", "骨头"]  （排序是为了稳定）
    
    处理过程：
        输入 ["猫吃鱼", "狗吃骨头"]
        → 逐句提取字符 → {"猫", "吃", "鱼", "狗", "骨", "头"}
        → 排序 → ["吃", "狗", "骨", "猫", "头", "鱼"]
    
    为什么用字符而不是词？
        中文分词很复杂（需要额外库），这里用单个字符做演示，
        原理完全一样，只是粒度更细。
    """
    
    # set() 是 python 内置数据结构"集合"，
    # 特点是：里面的元素不会重复（自动去重）
    # 比如 set("hello") → {"h", "e", "l", "o"}  （注意只有一个 'l'）
    vocab_set = set()
    
    # 遍历每一句话
    for sentence in all_sentences:
        # 把句子里的每个字符都加入集合
        # set.update() 把一个可迭代对象的所有元素添加进集合
        # 比如 vocab_set.update("你好") → 集合里多了 "你" 和 "好"
        vocab_set.update(sentence)
    
    # sorted() 是 python 内置函数，作用是把可迭代对象排序后返回列表
    # sorted({"c", "a", "b"}) → ["a", "b", "c"]
    # 排序的目的是让词汇表的顺序固定，不会每次运行都不一样
    return sorted(vocab_set)


def sentence_to_bow_vector(sentence, vocabulary):
    """
    把一句话变成词袋向量。
    
    词袋向量 (bag of words vector)：
      一个和词汇表一样长的列表。
      第 i 个位置的数字 = 词汇表中第 i 个词在这句话里出现的次数。
    
    参数:
        sentence: 一句话，比如 "猫吃鱼"
        vocabulary: 词汇表列表，比如 ["吃", "狗", "猫", "鱼"]
    
    返回:
        一个整数列表（向量），比如 [1, 0, 1, 1]
        意思是：吃出现1次, 狗出现0次, 猫出现1次, 鱼出现1次
    
    处理过程：
        输入 "猫吃鱼"，词汇表 ["吃", "狗", "猫", "鱼"]
        → 统计每个字符出现次数 → 吃:1, 狗:0, 猫:1, 鱼:1
        → 输出 [1, 0, 1, 1]
    """
    # 初始化一个全零的向量，长度 = 词汇表的长度
    # [0] * 5 会生成 [0, 0, 0, 0, 0]
    vector = [0] * len(vocabulary)
    
    # 遍历句子中的每个字符
    for char in sentence:
        # 如果这个字符在我们的词汇表里（不在的话就跳过）
        if char in vocabulary:
            # list.index(x) 是列表的内置方法，返回元素 x 在列表中的位置（索引）
            # 比如 ["a","b","c"].index("b") → 1
            idx = vocabulary.index(char)
            
            # 在向量的对应位置加 1（表示这个字符出现次数 +1）
            vector[idx] += 1
    
    return vector


