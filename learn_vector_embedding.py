# ================================================================================
#  第一部分：什么是向量？—— 先理解"数字表示"这个概念
# ================================================================================
#  向量 (vector) 就是一组有序的数字。
#  比如 [0.8, 0.3, -0.5] 是一个三维向量。
#  
#  核心思想：任何东西都可以用向量表示。
#  比如：
#    - 一个 rgb 颜色  →  [红值, 绿值, 蓝值]    （三维向量）
#    - 一个二维坐标    →  [x, y]               （二维向量）
#    - 一段文字        →  [0.12, -0.34, ...]   （几百维向量）
#  
#  颜色是最容易理解的例子，我们先从颜色向量开始：
# ================================================================================

