print("""
  余弦相似度 (cosine similarity)：
    两个向量之间夹角的余弦值。
    结果范围是 -1 到 1：
      1.0  → 方向完全一样（最相似）
      0.0  → 完全无关
      -1.0 → 方向完全相反
    
    和前面用的"欧几里得距离"不同：
      距离：关心数值大小
      余弦：关心方向是否一致（更关注比例而非绝对值）
    
    在文本处理中，余弦相似度比欧几里得距离更好用。
    原因：两篇长文章用了相似词汇 → 余弦相似度高，
          但它们的欧几里得距离可能因为文章长度不同而很大。
""")


def cal_cosine_similarity(vec_a, vec_b):
    """
    计算两个向量之间的余弦相似度。
    
    公式：cos(θ) = (a·b) / (|a| × |b|)
    
    其中：
      a·b  = 点积（两个向量对应位置相乘再求和）
      |a|  = 向量 a 的长度（也叫"模"或"范数"）
    
    参数:
        vec_a: 第一个向量（数字列表）
        vec_b: 第二个向量（数字列表）
    
    返回:
        一个浮点数，范围 -1.0 到 1.0
        1.0 = 方向完全相同
    
    举例:
        [1,0,0] 和 [1,0,0]    → 1.0    （完全一样）
        [1,0,0] 和 [0,1,0]    → 0.0    （完全不同）
        [1,2,3] 和 [2,4,6]    → 1.0    （方向一样，虽然数值不同）
    """
    
    # === 步骤1：计算点积 (dot product) ===
    # 点积 = 两个向量每个位置相乘，然后把所有乘积加起来
    # 比如 [a1,a2] · [b1,b2] = a1×b1 + a2×b2
    # 
    # zip() 同时取两个列表的元素
    # sum() 把所有的乘积加起来
    dot_product = sum(a * b for a, b in zip(vec_a, vec_b))
    
    # === 步骤2：计算向量 a 的长度（模） ===
    # 公式：|a| = sqrt(a1² + a2² + a3² + ...)
    # 每个元素平方，求和，再开根号
    # 
    # math.sqrt(x) 求 x 的平方根
    magnitude_a = math.sqrt(sum(a * a for a in vec_a))
    
    # === 步骤3：计算向量 b 的长度（模） ===
    magnitude_b = math.sqrt(sum(b * b for b in vec_b))
    
    # === 步骤4：检查是否有零向量 ===
    # 如果任何一个向量的长度是 0，说明这个向量全是零，
    # 没办法做除法，余弦相似度也无意义，返回 0
    if magnitude_a == 0 or magnitude_b == 0:
        return 0.0
    
    # === 步骤5：套公式计算余弦相似度 ===
    # cos = 点积 / (长度_a × 长度_b)
    cosine = dot_product / (magnitude_a * magnitude_b)
    
    # round() 是 python 内置函数，作用是四舍五入
    # round(3.14159, 4) → 3.1416
    # 这里保留4位小数，方便阅读
    return round(cosine, 4)


